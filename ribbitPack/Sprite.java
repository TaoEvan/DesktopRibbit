package ribbitPack;

class Sprite {
    public String name;
    public int x;
    public int y;
    public int width;
    public int height;

    public Sprite(String name, int x, int y, int width, int height) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;

    }


}